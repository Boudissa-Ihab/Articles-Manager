                    <p style="text-align:justify">
                        <span style="font-size: 16px;">
                            Le Journal de Préhistoire et
                            d'Ecologie d'Afrique du Nord accepte les articles qui
                            n'ont pas été soumis dans une autre revue. Les droits de
                            reproductions de l'article, des illustrations sont
                            réservés au Journal. La reproduction de l'article doit
                            faire l'objet d'une demande écrite au préalable,
                            adressée à la rédaction de la revue. Ceci est fait pour
                            éviter la multiplication sous d'autres formes de
                            l'article édité.
                        </span>
                    </p>
                    <p style="text-align:justify">
                        <span style="font-size: 16px;">
                            Le nombre de pages des manuscrits n'est
                            pas limité, mais la priorité sera donnée à des
                            textes courts (moins de 60&nbsp;000 signes), résumés et
                            bibliographie comprise. Le Journal PECAN accepte des notes
                            d'information pour signaler une découverte inédite ou
                            des résultats préliminaires, des mises au point critique, des
                            rapports de fouilles ou des résumés de
                            recherches.
                        </span>
                    </p>
                    <p style="text-align:justify">
                        <span style="font-size: 16px;">
                            Les articles acceptés seront soumis
                            à des relecteurs critiques (deux reviewers); si les auteurs le
                            demandent, ils peuvent suggérer des relecteurs appropriés. Les
                            remarques ou critiques doivent être suivies par l'auteur de
                            l'article.
                        </span>
                    </p>
                    <p style="text-align:justify">
                        <span style="font-size: 16px;">
                            Les manuscrits devront être rédiger
                            en suivant les recommandations rigoureuses et que les auteurs devront
                            adresser à l'adresse mail du Journal de Préhistoire et
                            d'Ecologie d'Afrique du Nord.
                        </span>
                    </p>
                    <p style="text-align:justify">
                        <span style="font-size: 16px;">
                            Tout manuscrit non conforme aux recommandations
                            sera retourné. Chaque texte est évalué par deux
                            rapporteurs voir plus.
                        </span>
                    </p>
                    <p style="text-align:justify">
                        <span style="font-size: 16px;">
                            Chaque manuscrit soumis à la
                            rédaction du Journal doit être présenté en format A4,
                            avec un double interligne et des marges de 3 cm et les pages doivent
                            être numéroter. Le texte définitif doit être
                            adresser en format numérique et ouvert (word), les tableaux, les
                            graphiques, les figures et les légendes sont à envoyer
                            séparément du texte, les illustrations (originales) sur Adob
                            Illustrator, Photoshop, les photographies (300 dpi minimum), les figures ou
                            dessins (1000 dpi minimum), police Times New Roman, corps 12.
                        </span>
                    </p>
                    <p style="text-align:justify">&nbsp;</p>
                    <p style="text-align:justify">
                        <b>
                            <span style="font-size: 20px;">
                                Format
                            </span>
                        </b>
                    </p>
                    <p style="text-align:justify">
                        <span style="font-size: 16px;">
                            Les manuscrits, dans les langues
                            déjà cités au stylez scientifique, doivent être
                            structurés en suivant les indications suivantes&nbsp;:
                        </span>
                    </p>
                    <ul style="list-style: circle inside;">
                        <li style="text-align:justify; margin-left: 15px;">
                            <span style="font-size: 16px;">
                                Le titre en français ou anglais&nbsp;;
                            </span>
                        </li>
                        <li style="text-align:justify; margin-left: 15px;">
                            <span style="font-size: 16px;">
                                Le (s) nom (s) et le (s) prénom (s)
                                de (s) auteur (s), l'adresse professionnelle, l'adresse
                                mail&nbsp;;
                            </span>
                        </li>
                        <li style="text-align:justify; margin-left: 15px;">
                            <span style="font-size: 16px;">
                                Le résumé en français, anglais,
                                arabe ou tamazight ne doit pas dépasser les 300
                                mots&nbsp;;
                            </span>
                        </li>
                        <li style="text-align:justify; margin-left: 15px;">
                            <span style="font-size: 16px;">
                                Les mots clés dans les langues
                                d'expression choisis&nbsp;;
                            </span>
                        </li>
                        <li style="text-align:justify; margin-left: 15px;">
                            <span style="font-size: 16px;">
                                La mise en forme italique pour les noms de
                                genres et d'espèces&nbsp;;
                            </span>
                        </li>
                        <li style="text-align:justify; margin-left: 15px;">
                            <span style="font-size: 16px;">
                                Le texte courant doit porter les
                                références aux illustrations et aux tableaux. Il faut les
                                indiquer comme suit&nbsp;: Fig. 1), (Figs 3&nbsp;; 5), (Fig.s 1-5),
                                (Tableau 1)&nbsp;;
                            </span>
                        </li>
                        <li style="text-align:justify; margin-left: 15px;">
                            <span style="font-size: 16px;">
                                Les légendes des figures,
                                données sur une feuille séparée doivent porter
                                toutes les indications (Echelle, les abréviations, le nom des
                                auteurs des cartes ou autres figures)
                            </span>
                        </li>
                        <li style="text-align:justify; margin-left: 15px;">
                            <span style="font-size: 16px;">
                                Il faut indiquer l'emplacement des figures, tableaux ou illustrations&nbsp;;
                            </span>
                        </li>
                        <li style="text-align:justify; margin-left: 15px;">
                            <span style="font-size: 16px;">
                                Il faut limiter les notes de bas de page et renvois&nbsp;;
                            </span>
                        </li>
                        <li style="text-align:justify; margin-left: 15px;">
                            <span style="font-size: 16px;">
                                Il faut respecter les normes des références bibliographiques.
                            </span>
                        </li>
                    </ul>
                    <p style="text-align:justify">&nbsp;</p>
                    <p style="text-align:justify">
