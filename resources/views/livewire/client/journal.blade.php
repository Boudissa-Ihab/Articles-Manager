                                Illustrations
                            </span>
                        </b>
                    </p>
                    <p style="text-align:justify">
                        <span style="font-size: 16px;">
                            Il est important de tenir compte de la
                            qualité des illustrations à intégrer dans le manuscrit.
                            Le support numérique dot répondre aux exigences minimales. Les
                            recommandations informatiques pour le traitement de l'image sont
                            à prendre en considération (300 dpi minimum, Photoshop,
                            Illustrator &hellip;). Les relevés ou photographies anciennes doivent
                            répondre aux conditions minimales (600 dpi). Les illustrations,
                            figures et cartes doivent porter une échelle métrique. Les
                            tableaux doivent être lisibles.
                        </span>
                    </p>
